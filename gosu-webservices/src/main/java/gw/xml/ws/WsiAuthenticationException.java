package gw.xml.ws;

public class WsiAuthenticationException extends RuntimeException {

  public WsiAuthenticationException() {
  }

  public WsiAuthenticationException( Throwable cause ) {
    super( cause );
  }

  public WsiAuthenticationException( String message ) {
    super( message );
  }

  public WsiAuthenticationException( String message, Throwable cause ) {
    super( message, cause );
  }
  
}
